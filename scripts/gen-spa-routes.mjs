#!/usr/bin/env node
/**
 * Emits nginx map of valid SPA paths → index.html for true HTTP 404 on unknown URLs.
 * Run at Docker build (and optionally prebuild).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function slugsFrom(file, re) {
  const text = fs.readFileSync(file, "utf8");
  const out = [];
  let m;
  const r = new RegExp(re, "g");
  while ((m = r.exec(text))) out.push(m[1]);
  return out;
}

const toolSlugs = slugsFrom(
  path.join(root, "src/config/site.config.js"),
  /slug:\s*"([^"]+)"/g
);
const articleSlugs = slugsFrom(
  path.join(root, "src/pages/Education.jsx"),
  /slug:\s*"([^"]+)"/g
);

const staticPaths = [
  "/",
  "/tools",
  "/education",
  "/glossary",
  "/resources",
  "/about",
  "/the-editor",
  "/contact",
  "/terms",
  "/privacy",
  "/start-here",
  "/tools/how-our-calculators-work",
];

const paths = new Set(staticPaths);
for (const s of toolSlugs) paths.add(`/tools/${s}`);
for (const s of articleSlugs) paths.add(`/education/${s}`);

const lines = [
  "# AUTO-GENERATED by scripts/gen-spa-routes.mjs — do not edit",
  "map $uri $spa_route {",
  "    default 0;",
];
for (const p of [...paths].sort()) {
  lines.push(`    ${p} 1;`);
  if (p !== "/" && !p.endsWith("/")) lines.push(`    ${p}/ 1;`);
}
lines.push("}", "");

const outDir = path.join(root, "nginx");
fs.mkdirSync(outDir, { recursive: true });
const outFile = path.join(outDir, "spa-routes.map");
fs.writeFileSync(outFile, lines.join("\n"));
console.log(`Wrote ${outFile} (${paths.size} paths)`);
